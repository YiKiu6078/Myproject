#include <iostream>
using namespace std;

int main(int argc, char const *argv[])
{
    bool a = true;
    bool b = false;
    cout<<"a = "<<a<<endl;
    cout<<"b = "<<b<<endl;

    bool c = -1;
    bool d = 0;
    cout<<"c = "<<c<<endl;
    cout<<"d = "<<d<<endl;
    cout<<"sizeof(bool) = "<<sizeof(bool)<<endl;
    return 0;
}
